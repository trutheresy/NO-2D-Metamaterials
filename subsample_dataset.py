"""
Subsample an existing PT dataset bundle.

This applies the same random reduced-index construction used in the
MATLAB->PT conversion workflow, but starts from a PT dataset generated by
generate_dispersion_dataset_Han_Alex.py.

Input directory is expected to contain:
- displacements_dataset.pt
- geometries_full.pt
- waveforms_full.pt
- wavevectors_full.pt
- band_fft_full.pt
- design_params_full.pt
- eigenvalue_data_full.pt

Output directory contains:
- displacements_dataset.pt          (reduced samples only)
- reduced_indices.pt               (list[(design_idx, wavevector_idx, band_idx)])
- geometries_full.pt               (full, unchanged copy)
- waveforms_full.pt                (full, unchanged copy)
- wavevectors_full.pt              (full, unchanged copy)
- band_fft_full.pt                 (full, unchanged copy)
- design_params_full.pt            (full, unchanged copy, optional offset)
- eigenvalue_data_full.pt          (full, unchanged copy; explicitly retained)
- eigenvalue_data_reduced.pt       (reduced frequencies aligned with reduced_indices)
- subsample_manifest.json
"""

from __future__ import annotations

import argparse
import json
from pathlib import Path

import numpy as np
import torch


def _require_file(path: Path) -> None:
    if not path.exists():
        raise FileNotFoundError(f"Missing required file: {path}")


def _build_reduced_indices(
    n_designs: int,
    n_wavevectors: int,
    n_bands: int,
    wvr: float,
    br: float,
    rng: np.random.Generator,
) -> list[tuple[int, int, int]]:
    n_wv_reduced = int(n_wavevectors * wvr)
    n_band_reduced = int(n_bands * br)
    if n_wv_reduced <= 0 or n_band_reduced <= 0:
        raise ValueError(
            f"Reduction produced empty subsets: n_wv_reduced={n_wv_reduced}, "
            f"n_band_reduced={n_band_reduced}. Increase --wvr/--br."
        )

    reduced_indices: list[tuple[int, int, int]] = []
    if wvr < 1.0 or br < 1.0:
        # Match convert_mat_to_pytorch.py behavior:
        # - sample wavevectors per design
        # - sample bands per selected wavevector
        for d_idx in range(n_designs):
            waveform_indices = rng.choice(n_wavevectors, size=n_wv_reduced, replace=False)
            for w_idx in waveform_indices.tolist():
                band_indices = rng.choice(n_bands, size=n_band_reduced, replace=False)
                for b_idx in band_indices.tolist():
                    reduced_indices.append((d_idx, int(w_idx), int(b_idx)))
    else:
        # No reduction: keep full sequential ordering.
        for d_idx in range(n_designs):
            for w_idx in range(n_wavevectors):
                for b_idx in range(n_bands):
                    reduced_indices.append((d_idx, w_idx, b_idx))
    return reduced_indices


def _flat_index(d_idx: int, w_idx: int, b_idx: int, n_wavevectors: int, n_bands: int) -> int:
    # Must match flatten order in generate_dispersion_dataset_Han_Alex.py:
    # for d in designs: for w in wavevectors: for b in bands
    return d_idx * (n_wavevectors * n_bands) + w_idx * n_bands + b_idx


def subsample_dataset(
    input_dir: Path,
    output_dir: Path,
    wvr: float,
    br: float,
    seed: int,
    design_offset: int,
) -> dict:
    required = [
        "displacements_dataset.pt",
        "geometries_full.pt",
        "waveforms_full.pt",
        "wavevectors_full.pt",
        "band_fft_full.pt",
        "design_params_full.pt",
        "eigenvalue_data_full.pt",
    ]
    for name in required:
        _require_file(input_dir / name)

    displacements = torch.load(input_dir / "displacements_dataset.pt", map_location="cpu")
    geometries_full = torch.load(input_dir / "geometries_full.pt", map_location="cpu")
    waveforms_full = torch.load(input_dir / "waveforms_full.pt", map_location="cpu")
    wavevectors_full = torch.load(input_dir / "wavevectors_full.pt", map_location="cpu")
    band_fft_full = torch.load(input_dir / "band_fft_full.pt", map_location="cpu")
    design_params_full = torch.load(input_dir / "design_params_full.pt", map_location="cpu")
    eigenvalue_data_full = torch.load(input_dir / "eigenvalue_data_full.pt", map_location="cpu")

    if not hasattr(displacements, "tensors") or len(displacements.tensors) != 4:
        raise ValueError("displacements_dataset.pt must be a TensorDataset with 4 tensors.")

    n_designs = int(geometries_full.shape[0])
    n_wavevectors = int(waveforms_full.shape[0])
    n_bands = int(band_fft_full.shape[0])
    expected_samples = n_designs * n_wavevectors * n_bands
    actual_samples = int(displacements.tensors[0].shape[0])
    if actual_samples != expected_samples:
        raise ValueError(
            f"Sample mismatch: expected {expected_samples} from full tensors, "
            f"but displacements_dataset has {actual_samples}."
        )

    rng = np.random.default_rng(seed)
    reduced_indices = _build_reduced_indices(
        n_designs=n_designs,
        n_wavevectors=n_wavevectors,
        n_bands=n_bands,
        wvr=wvr,
        br=br,
        rng=rng,
    )

    flat_indices = np.asarray(
        [_flat_index(d, w, b, n_wavevectors, n_bands) for d, w, b in reduced_indices],
        dtype=np.int64,
    )
    flat_indices_t = torch.from_numpy(flat_indices)

    reduced_tensors = tuple(t.index_select(0, flat_indices_t) for t in displacements.tensors)
    displacements_reduced = torch.utils.data.TensorDataset(*reduced_tensors)

    # Keep full eigenvalue tensor and also provide reduced-aligned frequencies.
    reduced_freq = torch.from_numpy(
        np.asarray(
            [eigenvalue_data_full[d, w, b].item() for d, w, b in reduced_indices],
            dtype=np.float32,
        )
    )

    output_dir.mkdir(parents=True, exist_ok=True)

    if design_offset != 0:
        design_params_full = design_params_full.clone()
        design_params_full[:, 0] = design_params_full[:, 0] + design_offset

    torch.save(displacements_reduced, output_dir / "displacements_dataset.pt")
    torch.save(reduced_indices, output_dir / "reduced_indices.pt")
    torch.save(geometries_full, output_dir / "geometries_full.pt")
    torch.save(waveforms_full, output_dir / "waveforms_full.pt")
    torch.save(wavevectors_full, output_dir / "wavevectors_full.pt")
    torch.save(band_fft_full, output_dir / "band_fft_full.pt")
    torch.save(design_params_full, output_dir / "design_params_full.pt")
    torch.save(eigenvalue_data_full, output_dir / "eigenvalue_data_full.pt")
    torch.save(reduced_freq, output_dir / "eigenvalue_data_reduced.pt")

    manifest = {
        "input_dir": str(input_dir),
        "output_dir": str(output_dir),
        "seed": int(seed),
        "wvr": float(wvr),
        "br": float(br),
        "design_offset": int(design_offset),
        "n_designs": n_designs,
        "n_wavevectors_full": n_wavevectors,
        "n_bands_full": n_bands,
        "n_samples_full": expected_samples,
        "n_samples_reduced": int(len(reduced_indices)),
        "retained_full_eigenvalue_data": True,
        "saved_files": [
            "displacements_dataset.pt",
            "reduced_indices.pt",
            "geometries_full.pt",
            "waveforms_full.pt",
            "wavevectors_full.pt",
            "band_fft_full.pt",
            "design_params_full.pt",
            "eigenvalue_data_full.pt",
            "eigenvalue_data_reduced.pt",
        ],
    }
    (output_dir / "subsample_manifest.json").write_text(json.dumps(manifest, indent=2), encoding="utf-8")
    return manifest


def main() -> None:
    parser = argparse.ArgumentParser(description="Subsample PT dataset using reduced-index workflow.")
    parser.add_argument("--input-dir", required=True, help="Input PT dataset folder.")
    parser.add_argument("--output-dir", required=True, help="Output folder for subsampled dataset.")
    parser.add_argument("--wvr", type=float, default=1.0, help="Wavevector reduction ratio in (0,1].")
    parser.add_argument("--br", type=float, default=1.0, help="Band reduction ratio in (0,1].")
    parser.add_argument("--seed", type=int, default=42, help="Random seed for reproducible sampling.")
    parser.add_argument("--do", type=int, default=0, help="Design index offset added to design_params_full.")
    args = parser.parse_args()

    if not (0.0 < args.wvr <= 1.0):
        raise ValueError("--wvr must be in (0, 1].")
    if not (0.0 < args.br <= 1.0):
        raise ValueError("--br must be in (0, 1].")

    manifest = subsample_dataset(
        input_dir=Path(args.input_dir),
        output_dir=Path(args.output_dir),
        wvr=float(args.wvr),
        br=float(args.br),
        seed=int(args.seed),
        design_offset=int(args.do),
    )
    print(json.dumps(manifest, indent=2))


if __name__ == "__main__":
    main()

